public class Destination {

    String ContractNumber;
    String Relation;
    String Institution;
    String MemberId;
    String TransitContractNumber;
    String Channel;

    public Destination() {
    }

    public String getContractNumber() {
        return ContractNumber;
    }

    public void setContractNumber(String contractNumber) {
        ContractNumber = contractNumber;
    }

    public String getRelation() {
        return Relation;
    }

    public void setRelation(String relation) {
        Relation = relation;
    }

    public String getInstitution() {
        return Institution;
    }

    public void setInstitution(String institution) {
        Institution = institution;
    }

    public String getMemberId() {
        return MemberId;
    }

    public void setMemberId(String memberId) {
        MemberId = memberId;
    }

    public String getTransitContractNumber() {
        return TransitContractNumber;
    }

    public void setTransitContractNumber(String transitContractNumber) {
        TransitContractNumber = transitContractNumber;
    }

    public String getChannel() {
        return Channel;
    }

    public void setChannel(String channel) {
        Channel = channel;
    }
}
